

def Pythagoras():
    AB=float(input("Enter the length of side AB:"))
    BC=float(input("Enter the length of side BC:"))
    AB=AB**2
    BC=BC**2
    CA=5.0
    CA=CA**2
    sum_of_sides=AB+BC
    print("proving (CA)^2=(AB)^2+(BC)^2")
    print(f"{CA}={sum_of_sides}")
    print(f"The square of hypotenuse is equal to the sum of the square of its perpendicular sides hence proved! ")
Pythagoras()